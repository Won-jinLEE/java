import java.util.Scanner;

public class fdasf1 {
	public static void main(String[] args) {
//스캐너 선언
		Scanner A = new Scanner(System.in);
//입력 메시지의 출력 선언 및 입력되는 수의 변수를 선언
		System.out.println("첫번째 숫자를 입력해주세요.");
		double Number1 = A.nextDouble();
		System.out.println("두번째 숫자를 입력해주세요.");
		double Number2 = A.nextDouble();
//식을 선언
		double DivisionNumber = Number1 / Number2;
		int CeDivisionNumber = (int) Math.ceil(DivisionNumber);
		int RoDivisionNumber = (int) Math.round(DivisionNumber);
		int FloDivisionNumber = (int) Math.floor(DivisionNumber);
//나눈 값, 올림, 반올림, 버림한 값의 출력을 선언
		System.out.println("나눈 값은:" + DivisionNumber + "입니다.");
		System.out.println("나머지를 올림한 값은" + CeDivisionNumber + "입니다.");
		System.out.println("나머지를 반올림한 값은" + RoDivisionNumber + "입니다.");
		System.out.println("나머지를 버림한 값은" + FloDivisionNumber + "입니다.");
	}
}